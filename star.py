import turtle
turtle.Screen().bgcolor("aqua")
turtle.Screen().setup(300,400)
p=turtle.Turtle()
p.forward(100)
p.left(120)
p.forward(100)
p.left(120)
p.forward(100)
p.penup()
p.right(150)
p.forward(50)
p.pendown()
p.right(90)
p.forward(100)
p.right(120)
p.forward(100)
p.right(120)
p.forward(100)
turtle.done()